import java.util.*;
class Natural_nos {
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int n,i,sum=0;
        System.out.println("Enter a number");
        n=sc.nextInt();
        for(i=1;i<=n;i++){
            sum+=i;
        }
        System.out.println(sum);
    }
}
